document.querySelector(".menu1").addEventListener('mouseover', function(e) {
  e.preventDefault();
  document.querySelector(".m-sub1").style.display = "block";
});
document.querySelector(".menu1").addEventListener('mouseleave', function(e) {
  e.preventDefault();
  document.querySelector(".m-sub1").style.display = "none";
});

