document.querySelector(".facil-item-img1").addEventListener('mouseover', function(e) {
  e.preventDefault();
  document.querySelector(".facil-item-name1").style.display = "block";
});
document.querySelector(".facil-item-img1").addEventListener('mouseleave', function(e) {
  e.preventDefault();
  document.querySelector(".facil-item-name1").style.display = "none";
});

